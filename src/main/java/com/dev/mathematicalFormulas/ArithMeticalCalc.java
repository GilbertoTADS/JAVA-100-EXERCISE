package com.dev.mathematicalFormulas;

public class ArithMeticalCalc {

    public static Integer sum(int i, int j) {
        return i+j;
    }

    public static Integer sub(int i, int j) {
        return i-j;
    }

    public static Integer mult(int i, int j) {
        return i*j;
    }

    public static Integer div(int i, int j) {
        return i/j;
    }

}
